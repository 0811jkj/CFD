    end
    
    
    if (y(j) <= integral(i+1))
    mtm_del_x_int_temp = mtm_del_x_int_temp + vel_x(j,i)/u_inf(i)*(1-vel_x(j,i)/u_inf(i))*dy;    
    end
    
